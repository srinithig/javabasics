package com.basic;

public class SwitchProgrammeExample {
public static void main(String[]args)
{
	int choice=30;
	switch(choice)
	{
	case 10:
	System.out.println(10);
	break;
	case 20:
	System.out.println(20);
	break;
	case 30:
	System.out.println(30);
	break;
	default:
	System.out.println("default");
	break;
	}
	}
	}