package com.basic;

public class SwitchPogramme1 {
	public static void main(String[]args) {
	int n = 2;
	switch (n)
	{
	case 1:
	System.out.println( "The number is 1" );
	case 2:
	System.out.println( "The number is 2" );
	break;
	default:
	System.out.println( "The number is not 1 or 2" );
	break;
	}
	}
}
